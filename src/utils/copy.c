void	copy_without_space(char *dest, const char *src)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (src[i])
	{
		if (src[i] != ' ')
		{
			dest[j] = src[i];
			j++;
		}
		i++;
	}
	dest[j] = '\0';
}